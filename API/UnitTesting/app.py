def add(x,y):
    return x+y

def sub(x,y):
    return x-y

def mul(x,y):
    return x*y

def div(x,y):
    return x/y

def main():
    result = add(10,2)
    print("Add", result)

    result = sub(10,2)
    print("Add", result)

    result = mul(10,2)
    print("Add", result)

    result = div(10,2)
    print("Add", result)
    
if __name__ == "__main__":
    main()